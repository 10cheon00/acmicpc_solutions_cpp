#include <iostream>
#include <cmath>
using namespace std;
int main() {
    int N, M, C = 0, D;
    cin >> N >> M;
    D = M - N;
    C = sqrt(D);
    if (D == 0) {
        cout << 0;
        return 0;
    }
    if (C*C == D) {
        cout << 2*C-1;
    } else if (C*C + C < D) {
        cout << 2*C+1;
    } else {
        cout << 2*C;
    }
}
