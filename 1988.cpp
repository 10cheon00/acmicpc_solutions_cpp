#include <iostream>
int dp[31]{
    1,
    0,
    3,
    0,
};
int f(int N) {
  if (N % 2) {
    return 0;
  }
  if (!dp[N]) {
    dp[N] = 3 * f(N - 2);
    for (int i = 4; i <= N; i += 2) {
      dp[N] += 2 * f(N - i);
    }
  }
  return dp[N];
}
int main() {
  int N;
  scanf("%d", &N);
  printf("%d\n", f(N));
}
